// TestCorpusManifestComplete checks that every manifest entry is internally consistent
// and present on disk, and that no committed .pdf under testdata/corpus lacks a manifest
// entry (an orphan fixture would look covered while being asserted on by nothing).
func TestCorpusManifestComplete(t *testing.T) {
	inManifest := map[string]bool{}
	for _, e := range corpusManifest {
		inManifest[filepath.ToSlash(e.Path)] = true
		checkManifestEntry(t, e)
	}
	err := filepath.WalkDir(corpusRoot, func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() || !strings.HasSuffix(path, ".pdf") {
			return nil
		}
		rel := filepath.ToSlash(strings.TrimPrefix(path, corpusRoot+string(filepath.Separator)))
		if !inManifest[rel] {
			t.Errorf("fixture on disk has no manifest entry: %s", rel)
		}
		return nil
	})
	if err != nil {
		t.Fatalf("walk corpus: %v", err)
	}
}
