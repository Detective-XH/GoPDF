func corpusPath(rel string) string { return filepath.Join(corpusRoot, filepath.FromSlash(rel)) }

func loadCorpus(t *testing.T, e corpusEntry) *Reader {
	t.Helper()
	data, err := os.ReadFile(corpusPath(e.Path))
	if err != nil {
		t.Fatalf("read fixture %s: %v", e.Path, err)
	}
	if e.Password != "" {
		r, err := NewReaderEncrypted(bytes.NewReader(data), int64(len(data)), func() string { return e.Password })
		if err != nil {
			t.Fatalf("open encrypted fixture %s: %v", e.Path, err)
		}
		return r
	}
	r, err := OpenBytes(data)
	if err != nil {
		t.Fatalf("open fixture %s: %v", e.Path, err)
	}
	return r
}

func extractPlainText(t *testing.T, r *Reader) string {
	t.Helper()
	rd, err := r.GetPlainText(context.Background())
	if err != nil {
		t.Fatalf("GetPlainText: %v", err)
	}
	b, err := io.ReadAll(rd)
	if err != nil {
		t.Fatalf("read extracted text: %v", err)
	}
	return string(b)
}

var wsRun = strings.NewReplacer("\r", " ", "\n", " ", "\t", " ")

func normalize(s string) string {
	return strings.Join(strings.Fields(wsRun.Replace(s)), " ")
}

// assertGolden checks extraction output `got` against golden bytes `want` per the
// entry's compare mode. Split out of TestCorpusGolden to keep that test's cyclomatic
// complexity within the gocyclo budget.
func assertGolden(t *testing.T, e corpusEntry, got string, want []byte) {
	t.Helper()
	switch e.Compare {
	case compareExact:
		if got != string(want) {
			t.Errorf("%s: exact mismatch\n got: %q\nwant: %q", e.Path, got, string(want))
		}
	case compareNormalized:
		// The golden holds one required snippet per non-empty line; EVERY one must
		// appear in the normalized extraction. Multiple body-text snippets (not just a
		// title) make this a real regression sentinel: an extractor that returns only
		// the heading but drops body text fails. An empty or whitespace-only golden
		// yields zero snippets and is rejected, because strings.Contains(x, "") is
		// vacuously true (D2/D3).
		gotNorm := normalize(got)
		var checked int
		for line := range strings.SplitSeq(string(want), "\n") {
			snip := normalize(line)
			if snip == "" {
				continue
			}
			checked++
			if !strings.Contains(gotNorm, snip) {
				t.Errorf("%s: required snippet not found in extraction\nsnippet: %q\ngot: %q",
					e.Path, snip, gotNorm)
			}
		}
		if checked == 0 {
			t.Fatalf("%s: normalized golden %s has no non-empty snippets — refusing to pass vacuously",
				e.Path, e.Golden)
		}
	default:
		// An out-of-range Compare would otherwise extract + read the golden but run no
		// assertion, making the entry look covered while regressions stay invisible.
		// TestCorpusManifestComplete also rejects this statically.
		t.Fatalf("%s: unknown compare mode %d", e.Path, int(e.Compare))
	}
}

func TestCorpusGolden(t *testing.T) {
	t.Parallel()
	for _, e := range corpusManifest {
		if e.Golden == "" {
			continue // bench-only fixture
		}
		t.Run(e.Path, func(t *testing.T) {
			t.Parallel()
			got := extractPlainText(t, loadCorpus(t, e))
			goldenFile := corpusPath(e.Golden)
			// -update regenerates synthetic+exact goldens only — never real/normalized
			// goldens, to avoid leaking PII from a real PDF's body (D3). The Synthetic
			// guard is belt-and-braces: a real entry mislabelled compareExact would
			// otherwise have its hand-authored golden overwritten by full extraction.
			// TestCorpusManifestComplete additionally rejects that mislabelling.
			if *updateGolden && e.Synthetic && e.Compare == compareExact {
				if err := os.WriteFile(goldenFile, []byte(got), 0o600); err != nil {
					t.Fatalf("update golden %s: %v", e.Golden, err)
				}
				return
			}
			//nolint:gosec // G304: goldenFile is a fixed corpus path from the manifest, not user input
			want, err := os.ReadFile(goldenFile)
			if err != nil {
				t.Fatalf("read golden %s: %v", e.Golden, err)
			}
			assertGolden(t, e, got, want)
		})
	}
}

// checkManifestEntry validates one manifest entry's compare-mode invariants and that
// its on-disk files exist. Split out of TestCorpusManifestComplete to keep that test's
// cyclomatic complexity within the gocyclo budget.
func checkManifestEntry(t *testing.T, e corpusEntry) {
	t.Helper()
	// Compare must be a known mode — an out-of-range value would slip past
	// TestCorpusGolden's switch with no assertion.
	if e.Compare != compareExact && e.Compare != compareNormalized {
		t.Errorf("manifest inconsistency: %s has unknown compare mode %d", e.Path, int(e.Compare))
	}
	// Invariant: synthetic ⇔ exact. A real fixture marked compareExact would be
	// overwritten by -update (D3 violation); a synthetic fixture marked
	// compareNormalized would skip byte-exact regression detection. Reject both.
	if e.Synthetic && e.Compare != compareExact {
		t.Errorf("manifest inconsistency: %s is Synthetic but not compareExact", e.Path)
	}
	if !e.Synthetic && e.Compare == compareExact {
		t.Errorf("manifest inconsistency: %s is real (non-synthetic) but compareExact — "+
			"-update would clobber its golden", e.Path)
	}
	if _, err := os.Stat(corpusPath(e.Path)); err != nil {
		t.Errorf("manifest fixture missing on disk: %s (%v)", e.Path, err)
	}
	if e.Golden != "" {
		if _, err := os.Stat(corpusPath(e.Golden)); err != nil {
			t.Errorf("manifest golden missing on disk: %s (%v)", e.Golden, err)
		}
	}
}

// TestCorpusNoGoldenFixtures guards the Golden == "" manifest entries against
// vacuous passes (adversarial-review finding): every such fixture must still
// open and paginate, and the hard-pdf negatives must still exhibit the
// extraction gap they document — replacement runes in the plain-text output.
// When future fallback-encoding work fixes one, this fails ON PURPOSE: promote
// the fixture to a golden-tested entry instead of relaxing the assertion.
func TestCorpusNoGoldenFixtures(t *testing.T) {
	t.Parallel()
	for _, e := range corpusManifest {
		if e.Golden != "" {
			continue
		}
		t.Run(e.Path, func(t *testing.T) {
			t.Parallel()
			r := loadCorpus(t, e)
			if n := r.NumPage(); n < 1 {
				t.Fatalf("NumPage = %d, want >= 1", n)
			}
			assertNoGoldenGap(t, e, r)
		})
	}
}

